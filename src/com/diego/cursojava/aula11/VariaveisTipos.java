package com.diego.cursojava.aula11;

public class VariaveisTipos {

	public static void main(String[] args) {
		
		// tipos interios (int, long)
		byte idade01 = 20;		
		short idade02 = 21;	
		int idade03 = 22;	
		long idade04 = 23;
		
		System.out.println("Valor da variavel idade01 = " + idade01);
		System.out.println("Valor da variavel idade02 = " + idade02);
		System.out.println("Valor da variavel idade03 = " + idade03);
		System.out.println("Valor da variavel idade04 = " + idade04 + "\n");
		
		
		// tipos flutuantes (double)
		float saldo01 = 100.30f;
		double saldo02 = 100.30;
		
		System.out.println("Valor da variavel saldo01 = " + saldo01);
		System.out.println("Valor da variavel saldo02 = " + saldo02 + "\n");
		
		
		// tipo caracter 
		char o = 'o';
		char i = 'i';
				
		System.out.println("" + o + i  + "\n");
		
		// tipo boleano (boolean)
		boolean verdadeiro = true;
		boolean falso = false;
		
		System.out.println(" Valor de verdadeiro " + verdadeiro);
		System.out.println(" Valor de falso " + falso + "\n");

		
		
	}

}
